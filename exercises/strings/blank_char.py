soccer_player = "\t\tMessi\t\t"
print(soccer_player + " is the best soccer player of the world.\n")
print(soccer_player.lstrip() + " is the best soccer player of the world.\n")
print(soccer_player.rstrip() + " is the best soccer player of the world.\n")
print(soccer_player.strip() + " is the best soccer player of the world.")
